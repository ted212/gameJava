public  class Character {

    private String Orc;
    private String Humain;


    public Character(String orc, String humain) {
        Orc = orc;
        Humain = humain;
    }

    public String getOrc() {
        return Orc;
    }

    public void setOrc(String orc) {
        Orc = orc;
    }

    public String getHumain() {
        return Humain;
    }

    public void setHumain(String humain) {
        Humain = humain;
    }
}
