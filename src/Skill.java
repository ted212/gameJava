public class Skill {

    private int Heall;
    private int Poison;

    public Skill(int heall, int poison) {
        Heall = heall;
        Poison = poison;
    }

    public int getHeall() {
        return Heall;
    }

    public void setHeall(int heall) {
        Heall = heall;
    }

    public int getPoison() {
        return Poison;
    }

    public void setPoison(int poison) {
        Poison = poison;
    }
}
